/*
Написать функцию, преобразующую число в объект. Передавая на вход число от 0 до 999,
мы должны получить на выходе объект, в котором в соответствующих свойствах описаны единицы, десятки и сотни.
Например, для числа 245 мы должны получить следующий объект: {‘единицы’: 5, ‘десятки’: 4, ‘сотни’: 2}.
Если число превышает 999, необходимо
выдать соответствующее сообщение с помощью console.log и вернуть пустой объект.
*/

//Решение

console.log('=== Задание № 1 ====');

function numberToObject(number) { //создаем конструктор объекта
    if (number > 999) { //внутри делаем условие на превышение определенного числа
        this.Результат = "Введите число меньше, чем 1000"; //выводим результат //можно и через console.log было, но решил так
    } else { // в обратном случае задаем объекту свойства
        this.единицы = Math.floor(number % 10); //расчет на количетсво единиц в числе
        this.десятки = Math.floor(number % 100 / 10); //расчет на количетсво десяток в числе
        this.сотни = Math.floor(number % 1000 / 100); //расчет на количетсво сотен в числе
        this.displayInfo = function () {  //дополнительный метод вывода в строку, так как при вызвое объекта выстраивает свойства по алфавиту
            console.log("Сотни: " + this.сотни + "; десятки: " + this.десятки + "; единицы: " + this.единицы);
        };
    };
};

var numbers = new numberToObject(536);//создаем объект
numbers.displayInfo(); // вызываем метод отображения строчного результата
console.log(numbers); //выводим объект

