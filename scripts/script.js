function product(name, image, price, description, buttonTobasket) { //создаем конструктор карточки товара
    this.name = name; //имя
    this.image = image; //картинка товара
    this.price = price; //цена
    this.description = description; //описание товара
    this.buttonTobasket = buttonTobasket; //кнопка добавить в корзину
};

let basket = []; //создаем корзину в виде пустого массива
let countBasketPrice = 0; //создаем переменную для вывода общей суммы товаров в корзине

